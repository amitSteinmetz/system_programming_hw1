/*  Return 1 (true) if a number is Armstrong number. 
An Armstrong number is an n-digit number that is equal to the sum of the n'th powers of its digits.
For Example: 407 = 43 + 03 + 73 = 64 + 0 + 343 = 407
*/
int isArmstrong(int);

/* Return 1 (true) if a number is a palindrome */ 
int isPalindrome(int);

/* Return 1 (true) if a number is prime*/ 
int isPrime(int);

/* Return 1 (true) if a number is Strong number
Strong number is a special number whose sum of the factorial of digits is equal to the original 
number. */
int isStrong(int);